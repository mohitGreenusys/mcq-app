const mongoose = require("mongoose");

const adminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// export default mongoose.model("Admin", adminSchema);
module.exports = mongoose.model("Admin", adminSchema);
